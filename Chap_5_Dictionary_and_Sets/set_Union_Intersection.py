
# UNION and INTERSECTION in Python

# What is UNION Python: It means take values from both sets and merge it without repeating elements

# so in Below Example 1 will be called only one 1 time because its a repeated value in set


s1 = {1,45,5}
s2 = {7,8,1,78}

print(s1.union(s2)) # Union Will Merge the Values

print(s1.intersection(s2)) # InterSection will take only Common values from both Sets


