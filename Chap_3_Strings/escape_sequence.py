#_________________ Escape Sequence Characters _________________________

# Escape Squence Characters always start with backSlash like \n \t

#Using \n Character (The text we will write after that will be go on next line)

description  = "Hello How are You! \n I am new in Python!"
print(description)

#Using \t Character (It will give the space Like TAB key)
print("To learn new things \t So join me")

#Using \""\ Character (The text we will write inside \"NAINI\"  quotation they will wrap it with quotation)
print("Enjoy to Learn \"Python\"")

